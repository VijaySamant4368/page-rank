import math
import matplotlib.pyplot as plt
import seaborn as sns

def list1d_to_square(lst):
    n = int(math.sqrt(len(lst)))
    if n*n != len(lst):
        raise ValueError("List length must be a perfect square")
    return [lst[i*n:(i+1)*n] for i in range(n)]

matrix = list1d_to_square(data)
n = len(matrix)

plt.figure(figsize=(7,7))
ax = sns.heatmap(
    matrix,
    cmap=["white","black"],  # binary colors
    vmin=0, vmax=1,
    square=True,
    cbar=False
)

labels = list(range(1, n + 1))

ax.set_xticks(range(n))
ax.set_yticks(range(n))

ax.set_xticklabels(labels)
ax.set_yticklabels(labels)

ax.xaxis.tick_top()

ax.set_xlabel("From")
ax.set_ylabel("To")

plt.show()